package day18_NestedLoop;

public class do_While_Loop2 {
    public static void main(String[] args) {

           // while loop

        int number = 103;
           //   while loop statement
        while (number >= 100){
            System.out.println(number);

            number--;
        }

        System.out.println("====================================");

        int number2 = 103;

       //          Do...While loop
        do{
            System.out.println(number2);
            number2--;

        }while (number2 >=100);






    }
}
