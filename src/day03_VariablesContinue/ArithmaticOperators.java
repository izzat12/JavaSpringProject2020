package day03_VariablesContinue;

public class ArithmaticOperators {

    public static void main(String[] args) {

        //+ - * / %

        System.out.println(5+3);          //addition
        System.out.println(10-7);             //subtraction
        System.out.println(10 * 2);            //multi
        System.out.println(10 / 2);      //division
    //    System.out.println( 9 / 0 );            //denominator cannot be zero in math

        System.out.println(10 % 3);        // 1
        System.out.println(100 % 33);             //1




    }




}
